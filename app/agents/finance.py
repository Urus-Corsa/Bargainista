async def run_independent(
    listing: ListingInput,
    db: AsyncSession,
) -> FinancePrecomputed:
    """Independent phase — runs in parallel with Vision and History nodes.

    Performs all computation that depends only on ListingInput and DB data:
    market value, depreciation projections, financing math, maintenance estimate,
    and the two LLM narrative fields (depreciation_summary, financing_vs_cash_analysis).

    Raises:
        RuntimeError:       If depreciation seed data is absent from the DB.
        anthropic.APIError: If the LLM narrative call fails.
    """
    current_year = datetime.now().year
    vehicle_age = max(0, current_year - (listing.year or current_year))

    # ------------------------------------------------------------------
    # 1. Classify vehicle into one of the 8 depreciation categories
    # ------------------------------------------------------------------
    category_name = classify_category(listing)
    logger.info(
        "Finance agent (independent): %s %s %s → category '%s'",
        listing.year, listing.make, listing.model, category_name,
    )

    # ------------------------------------------------------------------
    # 2. Load the three DB config layers
    # ------------------------------------------------------------------
    category = await db.scalar(
        select(DepreciationCategory).where(DepreciationCategory.name == category_name)
    )
    if category is None:
        logger.warning(
            "Category '%s' missing from DB — falling back to sedan curve", category_name
        )
        category = await db.scalar(
            select(DepreciationCategory).where(DepreciationCategory.name == "sedan")
        )
    if category is None:
        raise RuntimeError("Depreciation seed data is absent — run init_db() first")

    brand = await db.scalar(
        select(BrandModifier).where(
            BrandModifier.brand_name == (listing.make or "").lower().strip()
        )
    )

    all_variants = (
        await db.execute(
            select(VariantOverride).where(
                VariantOverride.make == (listing.make or "").lower().strip()
            )
        )
    ).scalars().all()

    # ------------------------------------------------------------------
    # 3. Three-layer market value
    # ------------------------------------------------------------------
    curve: list[float] = category.curve_values
    age_idx = min(vehicle_age, 10)

    brand_mod = brand.modifier if brand else 0.0
    matching_variants = [v for v in all_variants if _match_variant(listing, v)]
    variant_mod = sum(v.modifier for v in matching_variants)

    if matching_variants:
        logger.info(
            "Finance agent: %d variant override(s) matched (modifier total = %.3f)",
            len(matching_variants), variant_mod,
        )

    total_modifier = brand_mod + variant_mod
    typical_msrp = TYPICAL_MSRP[category_name]
    estimated_market_value = round(typical_msrp * curve[age_idx] * (1.0 + total_modifier))
    price_delta = listing.asking_price - estimated_market_value

    # ------------------------------------------------------------------
    # 4. Future value projections (+1, +3, +5 years from now)
    # ------------------------------------------------------------------
    def _future_value(years_ahead: int) -> int:
        idx = min(age_idx + years_ahead, 10)
        return round(typical_msrp * curve[idx] * (1.0 + total_modifier))

    projected_1yr = _future_value(1)
    projected_3yr = _future_value(3)
    projected_5yr = _future_value(5)

    # ------------------------------------------------------------------
    # 6. Financing math
    # ------------------------------------------------------------------
    apr = _loan_apr(vehicle_age)
    monthly_pmt = _monthly_payment(float(listing.asking_price), apr, _LOAN_TERM_MONTHS)
    total_paid = monthly_pmt * _LOAN_TERM_MONTHS
    total_interest = total_paid - listing.asking_price

    # ------------------------------------------------------------------
    # 7. Annual maintenance estimate
    # ------------------------------------------------------------------
    annual_maintenance = _annual_maintenance(category_name, vehicle_age)

    # ------------------------------------------------------------------
    # 9a. LLM narrative — depreciation and financing text only
    # ------------------------------------------------------------------
    price_delta_pct = (price_delta / max(estimated_market_value, 1)) * 100
    narrative_data = {
        "vehicle": f"{listing.year} {listing.make} {listing.model}",
        "vehicle_age_years": vehicle_age,
        "category": category_name,
        "estimated_market_value_usd": f"${estimated_market_value:,}",
        "market_range_usd": (
            f"${round(estimated_market_value * (1 - category.range_band)):,}"
            f" – ${round(estimated_market_value * (1 + category.range_band)):,}"
        ),
        "price_vs_market": (
            f"${abs(price_delta):,} {'above' if price_delta > 0 else 'below'} "
            f"midpoint ({price_delta_pct:+.1f}%)"
        ),
        "projected_value_1yr_usd": f"${projected_1yr:,}",
        "projected_value_3yr_usd": f"${projected_3yr:,}",
        "projected_value_5yr_usd": f"${projected_5yr:,}",
        "loan_term": f"{_LOAN_TERM_MONTHS} months (5 years)",
        "loan_apr": f"{apr * 100:.1f}%",
        "monthly_payment_usd": f"${round(monthly_pmt):,}",
        "total_interest_usd": f"${round(total_interest):,}",
        "total_loan_cost_usd": f"${round(total_paid):,}",
        "estimated_annual_maintenance_usd": f"${annual_maintenance:,}",
    }
    depreciation_summary, financing_vs_cash = await _generate_independent_narrative(narrative_data)

    return FinancePrecomputed(
        vehicle_age_years=vehicle_age,
        category_name=category_name,
        typical_msrp=typical_msrp,
        range_band=category.range_band,
        estimated_market_value=estimated_market_value,
        price_delta=price_delta,
        projected_1yr=projected_1yr,
        projected_3yr=projected_3yr,
        projected_5yr=projected_5yr,
        annual_maintenance=annual_maintenance,
        apr=apr,
        monthly_payment=monthly_pmt,
        total_interest=total_interest,
        total_paid=total_paid,
        depreciation_summary=depreciation_summary,
        financing_vs_cash_analysis=financing_vs_cash,
    )


async def run_dependent(
    listing: ListingInput,
    precomputed: FinancePrecomputed | None,
    vision_result: VisionAgentResult | None,
    history_result: HistoryAgentResult | None,
) -> FinanceAgentResult:
    """Dependent phase — runs after the three-way join (Vision, History, Finance-independent).

    Aggregates repair costs from both agents, computes the finance score,
    and generates the plain-language summary. If precomputed is None (independent
    phase failed), returns a degraded result with finance_score=None.

    None vision_result or history_result is handled gracefully — repair costs
    from that source default to zero and the summary notes the gap.
    """
    if precomputed is None:
        logger.warning("Finance dependent: precomputed is None — independent phase failed")
        return FinanceAgentResult(
            finance_score=None,
            estimated_market_value=0,
            price_delta=0,
            total_repair_cost_low=0,
            total_repair_cost_high=0,
            depreciation_summary=(
                "Market value computation unavailable — the independent finance phase failed. "
                "Depreciation and financing analysis cannot be provided for this vehicle."
            ),
            financing_vs_cash_analysis="Financing analysis unavailable.",
            projected_annual_maintenance=0,
            summary=(
                "Finance analysis could not be completed because the market value computation "
                "failed. The overall score is based on condition and history data only."
            ),
        )

    # ------------------------------------------------------------------
    # 5. Repair cost aggregation (Vision + History)
    #
    # Per-item MCP normalization: call get_repair_estimate for each item
    # concurrently via asyncio.gather.  If MCP returns available=True, use
    # its cost_low/cost_high; otherwise keep the item's LLM-estimated costs.
    #
    # paint_complexity multiplier: applied after MCP normalization, to
    # cosmetic-category Vision items only.  History items never receive it —
    # paint complexity is a visual property detected exclusively by Vision.
    # ------------------------------------------------------------------

    # Resolve paint complexity multiplier once (Vision items only)
    paint_complexity = vision_result.paint_complexity if vision_result else None
    if paint_complexity is not None:
        paint_mult = _PAINT_COMPLEXITY_MULTIPLIER.get(paint_complexity.value, 1.0)
    else:
        paint_mult = 1.0

    vision_repair_low = 0
    vision_repair_high = 0
    if vision_result:
        vision_items = vision_result.repair_items
        # Fire all MCP calls concurrently
        mcp_tasks = [
            call_tool(
                "get_repair_estimate",
                {
                    "make": listing.make,
                    "model": listing.model,
                    "year": listing.year,
                    "repair_type": item.component,
                },
            )
            for item in vision_items
        ]
        mcp_results = await asyncio.gather(*mcp_tasks)

        for item, mcp_result in zip(vision_items, mcp_results):
            # MCP normalization: use MCP costs if available, else LLM estimate
            if mcp_result is not None and mcp_result.get("available"):
                cost_low = mcp_result.get("cost_low", item.estimated_cost_low)
                cost_high = mcp_result.get("cost_high", item.estimated_cost_high)
            else:
                cost_low = item.estimated_cost_low
                cost_high = item.estimated_cost_high

            # paint_complexity multiplier — cosmetic items only
            if item.repair_category.value == "cosmetic":
                cost_low = round(cost_low * paint_mult)
                cost_high = round(cost_high * paint_mult)

            vision_repair_low += cost_low
            vision_repair_high += cost_high

    history_repair_low = 0
    history_repair_high = 0
    if history_result:
        history_items = history_result.repair_mentions
        # Fire all MCP calls concurrently
        mcp_tasks = [
            call_tool(
                "get_repair_estimate",
                {
                    "make": listing.make,
                    "model": listing.model,
                    "year": listing.year,
                    "repair_type": item.component,
                },
            )
            for item in history_items
        ]
        mcp_results = await asyncio.gather(*mcp_tasks)

        for item, mcp_result in zip(history_items, mcp_results):
            # MCP normalization only — no paint multiplier for History items
            if mcp_result is not None and mcp_result.get("available"):
                cost_low = mcp_result.get("cost_low", item.estimated_cost_low)
                cost_high = mcp_result.get("cost_high", item.estimated_cost_high)
            else:
                cost_low = item.estimated_cost_low
                cost_high = item.estimated_cost_high

            history_repair_low += cost_low
            history_repair_high += cost_high

    repair_low = vision_repair_low + history_repair_low
    repair_high = vision_repair_high + history_repair_high

    # ------------------------------------------------------------------
    # 8. Finance score (deterministic)
    # ------------------------------------------------------------------
    finance_score = _compute_finance_score(
        asking_price=listing.asking_price,
        market_value=float(precomputed["estimated_market_value"]),
        range_band=precomputed["range_band"],
        total_repair_high=repair_high,
        vehicle_age_years=precomputed["vehicle_age_years"],
        mileage=listing.mileage,
    )

    # ------------------------------------------------------------------
    # 9b. LLM summary — full context now available
    # ------------------------------------------------------------------
    price_delta = precomputed["price_delta"]
    price_delta_pct = (price_delta / max(precomputed["estimated_market_value"], 1)) * 100
    summary_data = {
        "vehicle": f"{listing.year} {listing.make} {listing.model}",
        "asking_price_usd": listing.asking_price,
        "estimated_market_value_usd": f"${precomputed['estimated_market_value']:,}",
        "price_vs_market": (
            f"${abs(price_delta):,} {'above' if price_delta > 0 else 'below'} "
            f"midpoint ({price_delta_pct:+.1f}%)"
        ),
        "total_repair_cost_low_usd": f"${repair_low:,}",
        "total_repair_cost_high_usd": f"${repair_high:,}",
        "effective_all_in_cost_usd": f"${listing.asking_price + repair_high:,}",
        "vision_data_available": "yes" if vision_result else "no (visual repair costs excluded)",
        "history_data_available": "yes" if history_result else "no (text repair costs excluded)",
        "finance_score": f"{finance_score}/10",
    }
    summary = await _generate_summary(summary_data)

    return FinanceAgentResult(
        finance_score=finance_score,
        estimated_market_value=precomputed["estimated_market_value"],
        price_delta=precomputed["price_delta"],
        total_repair_cost_low=repair_low,
        total_repair_cost_high=repair_high,
        depreciation_summary=precomputed["depreciation_summary"],
        financing_vs_cash_analysis=precomputed["financing_vs_cash_analysis"],
        projected_annual_maintenance=precomputed["annual_maintenance"],
        summary=summary,
    )


async def run(
    listing: ListingInput,
    vision_result: VisionAgentResult | None,
    history_result: HistoryAgentResult | None,
    db: AsyncSession,
) -> FinanceAgentResult:
    """Convenience wrapper — runs both phases in sequence.

    Used by direct callers (tests, manual scripts) that do not need the
    LangGraph split. The orchestrator calls run_independent and run_dependent
    from separate nodes; this wrapper preserves backward compatibility.
    """
    precomputed = await run_independent(listing, db)
    return await run_dependent(listing, precomputed, vision_result, history_result)
