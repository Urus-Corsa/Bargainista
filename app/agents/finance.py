"""Finance agent — vehicle value and cost-of-ownership analysis.

Computation is entirely in Python using the three-layer depreciation model:
    1. Category retention curve   (DB: depreciation_categories)
    2. Brand reliability modifier (DB: brand_modifiers)
    3. Variant/engine override    (DB: variant_overrides)

The LLM is called twice: once in the independent phase for depreciation and
financing narratives, once in the dependent phase for the overall summary.
No numbers are generated by the LLM; every numeric output is deterministic Python.

Entry points:
    run_independent(listing, db)                                     -> FinancePrecomputed
    run_dependent(listing, precomputed, vision_result, history_result) -> FinanceAgentResult
    run(listing, vision_result, history_result, db)                  -> FinanceAgentResult
        (convenience wrapper — calls both phases in sequence; used outside the graph)
"""

