async def _generate_independent_narrative(computed: dict) -> tuple[str, str]:
    """LLM call for depreciation_summary and financing_vs_cash_analysis.

    Runs in finance_independent_node — before Vision and History results are known.
    Returns (depreciation_summary, financing_vs_cash_analysis).
    """
    client = get_anthropic_client()
    lines = "\n".join(f"{k}: {v}" for k, v in computed.items())
    user_message = (
        "You are a vehicle finance analyst writing a report for a used-car buyer. "
        "Using the computed data below, produce the two narrative fields.\n\n" + lines
    )
    response = await client.messages.create(  # type: ignore[call-overload]
        model="claude-haiku-4-5-20251001",
        max_tokens=800,
        tools=[_INDEPENDENT_NARRATIVE_TOOL],  # type: ignore[list-item]
        tool_choice={"type": "tool", "name": "finance_independent_narrative"},
        messages=[{"role": "user", "content": user_message}],
    )
    for block in response.content:
        if block.type == "tool_use" and block.name == "finance_independent_narrative":
            dep = block.input.get("depreciation_summary", "Depreciation analysis unavailable.")
            fin = block.input.get("financing_vs_cash_analysis", "Financing analysis unavailable.")
            return dep, fin
    raise RuntimeError("Finance independent LLM did not return expected tool_use block")


