def _loan_apr(vehicle_age_years: int) -> float:
    """Estimate typical used-car loan APR by vehicle age.

    Source: Experian State of the Automotive Finance Market Q3 2023 averages.
    """
    if vehicle_age_years <= 3:
        return 0.075
    if vehicle_age_years <= 7:
        return 0.095
    return 0.115


def _monthly_payment(principal: float, annual_rate: float, n_months: int) -> float:
    """Standard loan amortization formula."""
    if principal <= 0:
        return 0.0
    if annual_rate == 0:
        return principal / n_months
    r = annual_rate / 12
    return principal * r * (1 + r) ** n_months / ((1 + r) ** n_months - 1)


def _annual_maintenance(category: str, vehicle_age_years: int) -> int:
    """Estimate annual maintenance cost with an age-band multiplier."""
    base = ANNUAL_MAINTENANCE_BASE.get(category, 700)
    if vehicle_age_years <= 3:
        multiplier = 0.70   # Likely under factory or CPO warranty
    elif vehicle_age_years <= 7:
        multiplier = 1.00   # RepairPal baseline age band
    else:
        multiplier = 1.45   # Aging components — brakes, belts, sensors
    return round(base * multiplier)


def _match_variant(listing: ListingInput, override: VariantOverride) -> bool:
    """Check whether a VariantOverride applies to this listing.

    Matching rules (must all pass):
      - make:             exact lowercase match
      - year:             within [year_from, year_to] if set
      - model_keywords:   at least one keyword found in the model string
      - engine_keywords:  at least one keyword found in model + description
                          (skipped if override.engine_keywords is empty)
    """
    make = (listing.make or "").lower()
    model = (listing.model or "").lower()
    search_text = model + " " + (listing.listing_description or "").lower()

    if override.make != make:
        return False
    if listing.year:
        if override.year_from and listing.year < override.year_from:
            return False
        if override.year_to and listing.year > override.year_to:
            return False
    if not any(kw in model for kw in override.model_keywords):
        return False
    if override.engine_keywords and not any(kw in search_text for kw in override.engine_keywords):
        return False
    return True


def _compute_finance_score(
    asking_price: int,
    market_value: float,
    range_band: float,
    total_repair_high: int,
    vehicle_age_years: int,
    mileage: int,
) -> int:
    """Deterministic 1-10 finance score. Anchored at 7 (fair deal).

    Adjustments:
      Price below market range:  up to +2 (buyer's discount)
      Price above market range:  up to -3 (seller's premium)
      High repair overhead:      up to -2 (repair cost / asking_price ratio)
      Mileage vs. expected:      ±0.5–1.0 (12,000 miles/year average)
    """
    market_low = market_value * (1 - range_band)
    market_high = market_value * (1 + range_band)
    score = 7.0

    if asking_price < market_low:
        discount = (market_low - asking_price) / market_low
        score += min(2.0, discount * 20)
    elif asking_price > market_high:
        premium = (asking_price - market_high) / max(market_high, 1)
        score -= min(3.0, premium * 15)

    if asking_price > 0:
        repair_ratio = total_repair_high / asking_price
        score -= min(2.0, repair_ratio * 8)

    expected_miles = vehicle_age_years * 12_000
    if expected_miles > 0:
        mileage_ratio = mileage / expected_miles
        if mileage_ratio > 1.5:
            score -= 1.0
        elif mileage_ratio > 1.2:
            score -= 0.5
        elif mileage_ratio < 0.5 and vehicle_age_years > 2:
            score += 0.5

    return max(1, min(10, round(score)))


