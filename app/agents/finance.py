def classify_category(listing: ListingInput) -> str:
    """Map a vehicle listing to one of the 8 depreciation categories.

    Resolution order matters: EV > minivan > truck > sports > SUV/sedan split.
    The SUV/sedan split is the final step and uses the luxury-make set.
    """
    make = (listing.make or "").lower().strip()
    model = (listing.model or "").lower().strip()

    if make in _EV_MAKES or any(t in model for t in _EV_MODEL_TOKENS):
        return "ev"
    if any(t in model for t in _MINIVAN_TOKENS):
        return "minivan"
    if make in _TRUCK_MAKES or any(t in model for t in _TRUCK_TOKENS):
        return "truck"
    if any(t in model for t in _SPORTS_TOKENS):
        return "sports"

    is_luxury = make in _LUXURY_MAKES

    if any(t in model for t in _SUV_TOKENS):
        return "luxury_suv" if is_luxury else "suv"

    return "luxury_sedan" if is_luxury else "sedan"


